# Should I walk or drive to my destination?
# Integer variable (stores whole number)
distance_meters = 200

# Boolean variable (stores True or False)
is_raining = False

# Comparison Operator:
# >  means "greater than"
#
# Logical Operator:
# or  returns True if at least one condition is True
#
# Parentheses are used to group expressions
should_drive = is_raining or (distance_meters > 1000)

# print() displays output to console
print("Should I drive?", should_drive)